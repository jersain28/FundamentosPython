#Operadores booleanos
#> >=
print(10 > 10)
print(10 >= 10)
#< <=
print(10 < 10)
print(10 <= 10)
#==
print(10 == 10)
#!=
print(10 != 10)

#Operadores Logicos
#and (True if all true)
print(True and True)
print(True and False)
#or (True if al least one true)
print(True or True)
print(True or False)
#not (not true)
print(not True)
print(not False)